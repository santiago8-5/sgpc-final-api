package com.grupoingenios.sgpc.sgpc_api_final.entity.inventory;

/**
 * Enum que representa los diferentes tipos de unidades para los insumos en el sistema de inventarios.
 * Los tipos de unidades disponibles son:
 * - **PZA:** Pieza.
 * - **KG:** Kilogramo.
 * - **TN:** Tonelada.
 * - **M:** Metro.
 * - **P:** Pulgada.
 * - **CM:** Centímetro.
 * - **G:** Gramo.
 */
public enum UnitType {
    /**
     * Unidad de medida en piezas.
     */
    PZA,
    /**
     * Unidad de medida en kilogramos.
     */
    KG,
    /**
     * Unidad de medida en toneladas.
     */
    TN,
    /**
     * Unidad de medida en metros.
     */
    M,
    /**
     * Unidad de medida en pulgadas.
     */
    P,
    /**
     * Unidad de medida en centímetros.
     */
    CM,
    /**
     * Unidad de medida en gramos.
     */
    G
}
