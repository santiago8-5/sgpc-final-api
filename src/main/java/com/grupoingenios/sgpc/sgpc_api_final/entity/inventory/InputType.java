package com.grupoingenios.sgpc.sgpc_api_final.entity.inventory;

/**
 * Enum que representa los tipos de insumo dentro del sistema de inventarios.
 * Los tipos de insumo son:
 * - **MATERIAL:** Representa un material en el inventario.
 * - **HERRAMIENTA_MENOR:** Representa una herramienta menor en el inventario.
 * - **HERRAMIENTA_MAYOR:** Representa una herramienta mayor en el inventario.
 */
public enum InputType {
    /**
     * Representa un material en el inventario.
     */
    MATERIAL,

    /**
     * Representa una herramienta menor en el inventario.
     */
    HERRAMIENTA_MENOR,

    /**
     * Representa una herramienta mayor en el inventario.
     */
    HERRAMIENTA_MAYOR
}

